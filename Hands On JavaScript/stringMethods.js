// Note: 
// All string methods return a new string.
// There are ten types of string methods in JavaScript which are widely used. GIven below:
// slice(), substring(), substr(), replace(), replaceAll(), toLowerCase(), toUpperCase(), trim(), charAt() and split().
// slice(), substring() and substr() are similar.
// slice method structure: slice(starting index, up to index). If parameter is negative value it will count from right side.
// substring() is also similar of slice() but it doesn't count negative value.
// substr() is also similar but structure is different. Let's see: substr(staring index, count).
// replace() method structure: replace(who will bereplaced, by which will be replaced). It replaces only the first match.
// For convert a string to lower case we will use toLowercase() method.
// For convert a string to upper case we will use toUpperCase() method.
// trim() method removes all irrelevant spaces from a string.
// charAt() finds a character according to a given index in the parameter from a string.
// split() method converts a string to an array according to given parameter like split("").



// String search methods:
// There are eight types of search methods in string. They are given below:
// indexOf(), lastIndexOf(), search(), match(), matchAll(), includes(), startsWith(), endsWith().
// indexOf() method returns the index of (position of) the first occurrence of a string in a string.
// lastIndexOf() method returns the index of the last occurrence of a specified text in a string.
// indexOf() and lastIndexOf() return -1 if the text is not found.
// search() method searches a string for a string (or a regular expression) and returns the position of the match.
// indexOf() and search() are not equal. search() method cannot take second arguement from which it will count on the other hand indexOf() cannot take powerful string like regular expression.
// match() method returns an array containing the results of matching a string against a string (or a regular expression).
// matchAll() method returns an iterator containing the results of matching a string against a string (or a regular expression).
// includes() method returns true if a string contains a specified value. Otherwise it returns false.This method is case sensitive.
// startsWith() method returns true if a string begins with a specified value. Otherwise it returns false. This method is case sensitive.
// endsWith() method returns true if a string ends with a specified value. Otherwise it returns false. This method is case sensitive.